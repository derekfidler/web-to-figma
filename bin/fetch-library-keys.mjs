#!/usr/bin/env node
/**
 * Hit Figma's REST API to enumerate published text + paint styles in the
 * two Flatpay library files, then write a manifest of keys the plugin
 * imports at runtime. Run this whenever the libraries change.
 *
 * Usage:
 *   FIGMA_TOKEN=figd_xxx node bin/fetch-library-keys.mjs
 *
 * Output: packages/plugin/src/library-manifest.ts
 */

import { writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));

const FILES = [
  { name: 'Web UI Kit', key: 'sMgZrmavDMcHlitc0AGdzW' },
  { name: 'Colours / Themes', key: 'Fpv4DA0L59L5gwlKC5YY8F' },
];

const TOKEN = process.env.FIGMA_TOKEN;
if (!TOKEN) {
  console.error('Missing FIGMA_TOKEN env var.');
  console.error('Generate at https://www.figma.com/developers/api#access-tokens and re-run:');
  console.error('  FIGMA_TOKEN=figd_xxx node bin/fetch-library-keys.mjs');
  process.exit(1);
}

async function listStyles(fileKey) {
  const resp = await fetch(`https://api.figma.com/v1/files/${fileKey}/styles`, {
    headers: { 'X-Figma-Token': TOKEN },
  });
  if (!resp.ok) {
    throw new Error(`Figma API ${resp.status} for file ${fileKey}: ${await resp.text()}`);
  }
  const body = await resp.json();
  return body.meta?.styles ?? [];
}

async function main() {
  const textStyles = [];
  const paintStyles = [];

  for (const file of FILES) {
    console.log(`Fetching styles from ${file.name} (${file.key})...`);
    let styles;
    try {
      styles = await listStyles(file.key);
    } catch (err) {
      console.error(`  failed: ${err.message}`);
      continue;
    }
    let textCount = 0;
    let paintCount = 0;
    for (const s of styles) {
      const entry = { key: s.key, name: s.name, source: file.name };
      if (s.style_type === 'TEXT') {
        textStyles.push(entry);
        textCount++;
      } else if (s.style_type === 'FILL') {
        paintStyles.push(entry);
        paintCount++;
      }
    }
    console.log(`  ${textCount} text styles, ${paintCount} paint styles`);
  }

  const out = `/**
 * Auto-generated by bin/fetch-library-keys.mjs — do not edit by hand.
 * Regenerate when the Web UI Kit or Colours/Themes libraries change.
 */
export type LibraryStyleEntry = { key: string; name: string; source: string };

export const LIBRARY_TEXT_STYLES: LibraryStyleEntry[] = ${JSON.stringify(textStyles, null, 2)};

export const LIBRARY_PAINT_STYLES: LibraryStyleEntry[] = ${JSON.stringify(paintStyles, null, 2)};
`;

  const path = resolve(__dirname, '..', 'packages/plugin/src/library-manifest.ts');
  await writeFile(path, out);
  console.log(`\n✓ Wrote ${textStyles.length} text + ${paintStyles.length} paint style keys → ${path}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
